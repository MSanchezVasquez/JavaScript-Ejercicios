let nombre = "Oliva";
let apellido = "Ordoñez";
let frase = `Su nombre es: ${nombre} y su apellido es: ${apellido}.`;
console.log(frase); //Su nombre es: Oliva y su apellido es: Ordoñez.

nombre = "Moises";
apellido = "Sanchez";
frase = `Mi nombre es: ${nombre} y mi apellido es: ${apellido}.`;
console.log(frase); //Mi nombre es: Moises y mi apellido es: Sanchez.
