// Ejercicio 95: Calcular la Raíz Cuadrada de un Número con la Función Math.sqrt
// sqrt => Square Root

console.log(Math.sqrt(5)); //2.23606797749979
console.log(Math.sqrt(100)); //10
console.log(Math.sqrt(2)); //1.4142135623730951
console.log(Math.sqrt(-2)); //NaN
console.log(Math.sqrt(null)); //0
console.log(Math.sqrt("100")); //10
console.log(Math.sqrt("SquareRoot")); //NaN
