// Ejercicio 324: Convertir un Número Decimal en un Número Hexadecimal.

// Número en base 10:
let decimal = 255;

// Número en base 16:
let resultado = decimal.toString(16);

console.log(decimal); //255
console.log(resultado); //ff
console.log("------------");
decimal = 16;
resultado = decimal.toString(16);
console.log(decimal); //16
console.log(resultado); //10
