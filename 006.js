// Ejercicio 6: Uso de los operadores relacionales.
// <, >, <=, >=, ==, ===

let numero = 8;

if(numero < 5){
    console.log("Numero es menor a 5")
}else{
    console.log("Numero no es menor a 5")
}

console.log("---------------------------------------");
if(numero <= 5){
    console.log("Numero es menor o igual a 5")
}else{
    console.log("Numero no es menor o igual a 5")
}

console.log("---------------------------------------");
if(numero > 5){
    console.log("Numero es mayor a 5")
}else{
    console.log("Numero no es mayor a 5")
}

console.log("---------------------------------------");
if(numero >= 5){
    console.log("Numero es mayor o igual a 5");
}else{
    console.log("Numero no es mayor o igual a 5");
}