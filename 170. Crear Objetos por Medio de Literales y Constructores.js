// Ejercicio 170: Crear Objetos por Medio de Literales y Constructores.

//Constructor:
let persona1 = new Object();
persona1.nombre = "Angela";
persona1.email = "angela.com";
persona1.edad = 35;
console.log(persona1); //{ nombre: 'Angela', email: 'angela.com', edad: 35 }

//Literal:
let persona2 = { nombre: "Moises", email: "moises.com", edad: 22 };
console.log(persona2); //{ nombre: 'Moises', email: 'moises.com', edad: 22 }
