// Ejercicio 4: Validar si una variable es undefined o null.

let numero = undefined;

if(numero === undefined){
    console.log("La variable numero es undefined.")
}

if(numero === null){
    console.log("La variable numero es null.")
}

numero = null;
if(numero === null){
    console.log("La variable numero es null.")
}

