// Ejercicio 149: Computar la Posición o Índice un Elemento en un Arreglo.

let numeros = [1, 2, 3, 4, 5];

let resultado = numeros.indexOf(3);
console.log(resultado); // Posicion 2

resultado = numeros.indexOf(5);
console.log(resultado); // Posicion 4

resultado = numeros.lastIndexOf(1);
console.log(resultado); // Posicion 0
