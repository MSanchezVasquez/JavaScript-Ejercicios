// Ejercicio 97: Estudiar la Diferencia entre las Funciones floor y ceil de la Clase Math

let numero = 1.65;
console.log(Math.floor(numero)); //1
console.log(Math.ceil(numero)); //2
console.log();
numero = 1.24;
console.log(Math.floor(numero)); //1
console.log(Math.ceil(numero)); //2
console.log();
numero = -3.6;
console.log(Math.floor(numero)); //-4
console.log(Math.ceil(numero)); //-3
